#!/usr/bin/env node
// Enforces MS029: apps/mobile/ios/ is the Continuous Native Generation output
// dir and must not be tracked by git. Two checks:
//
//   1. `git check-ignore --no-index apps/mobile/ios` exits 0 — the ignore
//      rules WOULD match the path. The --no-index flag is load-bearing:
//      without it, git skips the ignore check for any path already in the
//      index, which collapses this check into the second one and produces a
//      confusing dual failure. With --no-index the question is purely
//      "would the rules apply", which is what we want. Works on
//      non-existent paths (the dir is regenerated by prebuild and may not
//      exist on a clean checkout), and resolves the full ignore precedence
//      chain (root .gitignore, apps/mobile/.gitignore, parent ignores,
//      .git/info/exclude, negations).
//
//   2. `git ls-files apps/mobile/ios` returns no entries — even when the
//      path is gitignored, files added before the ignore was in place stay
//      tracked until `git rm -r --cached` runs.
//
// The two checks are independent: a working repo passes both; a broken
// configuration fails at least one. Trying to derive either signal from
// parsing .gitignore text by hand is fragile (multiple sources, negation
// rules, parent-dir inheritance), so we shell out to git, which is the
// only oracle that resolves precedence the same way every other tool does.
//
// Phase 6 (refactor/v7): exposes a rootDir-parameterised core function so
// `pnpm surface:audit` can run the same check against a foreign project.
// CLI behavior preserved when invoked without --root.

import { execFileSync } from "node:child_process";
import { resolve, relative, join } from "node:path";
import { fileURLToPath } from "node:url";
import { parseArgs } from "node:util";
import { buildReport, emitDiagnosticReport } from "./lib/diagnostics.mjs";
import { discoverAppConfig } from "./lib/app-config.mjs";

const TOOL = "check-ios-gitignore";

// True when `cwd` sits inside a git working tree. In audit mode a foreign
// project that is not under git is not an MS029 violation; there is no
// tracked-vs-ignored question to answer.
function isGitWorkTree(cwd) {
  try {
    execFileSync("git", ["rev-parse", "--is-inside-work-tree"], {
      stdio: "pipe",
      cwd,
    });
    return true;
  } catch {
    return false;
  }
}

/**
 * Run the MS029 gate against the git working tree at `rootDir`. Returns a
 * DiagnosticReport; the caller renders it.
 *
 * Check mode targets the fixed monorepo path apps/mobile/ios and treats a git
 * failure as a real fault. Audit mode discovers the Expo app directory so a
 * foreign project with ios/ at its root is handled, and treats a project that
 * is not a git working tree as not-applicable rather than a failure.
 *
 * @param {{ rootDir?: string, mode?: "check" | "audit" }} [options]
 */
export function checkIosGitignore({
  rootDir = process.cwd(),
  mode = "check",
} = {}) {
  const cwd = resolve(rootDir);
  const audit = mode === "audit";

  // Resolve the generated ios/ directory relative to the audited root. Check
  // mode uses the fixed monorepo layout; audit mode discovers the Expo app
  // directory so a root-shaped foreign project (ios/ at the project root) is
  // handled instead of false-failing against a hard-coded apps/mobile path.
  let targetPath = "apps/mobile/ios";
  if (audit) {
    const discovered = discoverAppConfig(cwd);
    if (!discovered.found) {
      return buildReport(TOOL, [
        {
          id: "ios-path-located",
          status: "fail",
          summary: `No Expo app config found under ${cwd}; cannot locate the generated ios/ directory.`,
        },
      ]);
    }
    targetPath = relative(cwd, join(discovered.mobileRoot, "ios")) || "ios";
  }

  // In audit mode a project that is not a git working tree has no MS029
  // question to answer. Check mode keeps the strict behavior.
  if (audit && !isGitWorkTree(cwd)) {
    return buildReport(TOOL, [
      {
        id: "ios-gitignore",
        status: "ok",
        summary: `${cwd} is not a git working tree; MS029 (ios/ gitignored and untracked) is not applicable.`,
        trapId: "MS029",
      },
    ]);
  }

  const checks = [];

  // Check 1: the path is gitignored.
  let ignored = false;
  let ignoreErr;
  try {
    execFileSync("git", ["check-ignore", "--no-index", targetPath], {
      stdio: "pipe",
      cwd,
    });
    ignored = true;
  } catch (err) {
    if (err.status === 1) {
      ignored = false;
    } else {
      ignoreErr = err.stderr?.toString().trim() || err.message;
    }
  }

  if (ignoreErr) {
    checks.push({
      id: "ios-path-gitignored",
      status: "fail",
      summary: `git check-ignore failed: ${ignoreErr}`,
      detail: {
        message:
          "Could not consult git to verify ignore rules. Ensure this script runs from a working tree inside the repository.",
      },
    });
  } else {
    checks.push({
      id: "ios-path-gitignored",
      status: ignored ? "ok" : "fail",
      summary: ignored
        ? `${targetPath} is gitignored.`
        : `${targetPath} is not gitignored.`,
      trapId: "MS029",
      ...(ignored
        ? {}
        : {
            detail: {
              message: `Add \`/ios\` to apps/mobile/.gitignore (or \`apps/mobile/ios/\` to the root .gitignore). The CNG-managed iOS project is regenerated by \`pnpm mobile:prebuild:ios\`; tracking it produces diffs nobody intended and fights prebuild on every refresh.`,
            },
          }),
    });
  }

  // Check 2: no files are currently tracked under the path. A path can be
  // ignored AND have tracked files (the ignore takes effect for new files
  // only; existing entries stay tracked until untracked explicitly).
  let trackedFiles = [];
  let lsErr;
  try {
    const out = execFileSync("git", ["ls-files", targetPath], {
      stdio: "pipe",
      cwd,
    });
    trackedFiles = out
      .toString()
      .split("\n")
      .map((line) => line.trim())
      .filter(Boolean);
  } catch (err) {
    lsErr = err.stderr?.toString().trim() || err.message;
  }

  if (lsErr) {
    checks.push({
      id: "ios-path-untracked",
      status: "fail",
      summary: `git ls-files failed: ${lsErr}`,
    });
  } else {
    const ok = trackedFiles.length === 0;
    checks.push({
      id: "ios-path-untracked",
      status: ok ? "ok" : "fail",
      summary: ok
        ? `${targetPath} has no tracked files in git.`
        : `${targetPath} has ${trackedFiles.length} tracked file(s) despite the ignore rule.`,
      trapId: "MS029",
      ...(ok
        ? {}
        : {
            detail: {
              message: `Run \`git rm -r --cached ${targetPath}\` and commit the deletion. The ignore rule only applies to new files; existing tracked entries must be removed explicitly.`,
              paths: trackedFiles.slice(0, 10),
            },
          }),
    });
  }

  return buildReport(TOOL, checks);
}

const isDirectInvocation =
  process.argv[1] && fileURLToPath(import.meta.url) === resolve(process.argv[1]);

if (isDirectInvocation) {
  const { values } = parseArgs({
    options: {
      json: { type: "boolean", default: false },
      root: { type: "string" },
      mode: { type: "string", default: "check" },
    },
  });
  const report = checkIosGitignore({
    rootDir: values.root ?? process.cwd(),
    mode: values.mode === "audit" ? "audit" : "check",
  });
  emitDiagnosticReport(report, { json: values.json });
}
