#!/usr/bin/env node
// Codegen for MobileSurfacesActivityAttributes.swift.
//
// Source of truth: the Zod schemas in packages/surface-contracts/src/schema.ts
//   - liveSurfaceActivityContentState (the ContentState fields)
//   - liveSurfaceStage (the Stage enum cases)
//
// Emits the Swift attribute file byte-identical to BOTH:
//   - packages/live-activity/ios/MobileSurfacesActivityAttributes.swift
//   - apps/mobile/targets/widget/MobileSurfacesActivityAttributes.swift
//
// Why this exists: ActivityKit silently fails when the ContentState shape
// drifts between the Expo module's binary and the widget extension's binary
// (MS002 / MS003 / MS004). The two files have to be byte-identical and they
// have to match the Zod contract. Phase 5 of the 2026-04 refactor planned a
// single shared Swift Package, but that path is upstream-blocked on
// `@bacons/apple-targets` local-SPM support + RN 0.84 `spm_dependency`
// local-path support. Codegen is the intermediate state that gives us a
// single source of truth (the Zod schema) without waiting on upstream. When
// SPM unblocks, this script gets deleted and the byte-identity check goes
// with it.
//
// The outer Attributes fields (`surfaceId`, `modeLabel`) are Swift-side
// metadata passed to `Activity.request(attributes:)`. They are not part of
// the wire payload that gets pushed via APNs (only ContentState is), so
// they are not in the Zod schema. They live in the fixed template surface
// in this file; treat them as "config" of the codegen.
//
// Run modes:
//   default     write generated Swift to both target paths
//   --check     fail with exit 1 if either committed file differs from the
//               generator output (mirrors scripts/build-schema.mjs and
//               scripts/build-agents-md.mjs)
//   --print     dump the generated Swift to stdout without writing
//
// Run via `node --experimental-strip-types` so the TS schema imports
// directly without a build step.
import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { resolve } from "node:path";
import { parseArgs } from "node:util";
import {
  liveSurfaceActivityContentState,
  liveSurfaceStage,
} from "../packages/surface-contracts/src/schema.ts";
import { resolveExpectedSwiftType } from "./lib/swift-content-state.mjs";

const { values } = parseArgs({
  options: {
    check: { type: "boolean", default: false },
    print: { type: "boolean", default: false },
  },
});

const TARGETS = [
  resolve("packages/live-activity/ios/MobileSurfacesActivityAttributes.swift"),
  resolve("apps/mobile/targets/widget/MobileSurfacesActivityAttributes.swift"),
];

const generated = renderSwift();

if (values.print) {
  process.stdout.write(generated);
  process.exit(0);
}

if (values.check) {
  let drift = false;
  for (const target of TARGETS) {
    const current = existsSync(target) ? readFileSync(target, "utf8") : "";
    if (current !== generated) {
      console.error(
        `${target.replace(`${process.cwd()}/`, "")} is out of sync with the Zod source of truth.`,
      );
      drift = true;
    }
  }
  if (drift) {
    console.error(
      "Run: node --experimental-strip-types --no-warnings=ExperimentalWarning scripts/generate-activity-attributes.mjs",
    );
    process.exit(1);
  }
  console.log(
    "MobileSurfacesActivityAttributes.swift is in sync with packages/surface-contracts/src/schema.ts.",
  );
  process.exit(0);
}

for (const target of TARGETS) {
  writeFileSync(target, generated);
  console.log(`Wrote ${target.replace(`${process.cwd()}/`, "")}.`);
}

// ---------- Generator ----------

function renderSwift() {
  const contentFields = renderContentStateFields();
  const stageCases = renderStageCases();

  return [
    "// AUTO-GENERATED by scripts/generate-activity-attributes.mjs. DO NOT EDIT.",
    "//",
    "// Source of truth: packages/surface-contracts/src/schema.ts",
    "//   - liveSurfaceActivityContentState defines the ContentState fields below.",
    "//   - liveSurfaceStage defines the Stage enum cases below.",
    "//",
    "// MUST stay byte-identical across the Expo module and widget target:",
    "//   packages/live-activity/ios/MobileSurfacesActivityAttributes.swift",
    "//   apps/mobile/targets/widget/MobileSurfacesActivityAttributes.swift",
    "//",
    "// ActivityKit binds the type used in `Activity<T>.request` (main app) to the",
    "// type used in `ActivityConfiguration(for: T.self)` (widget extension) by",
    "// matching ContentState/Attributes shape. The two definitions live in",
    "// different Swift modules at compile time but must serialize identically.",
    "//",
    "// To change ContentState fields or Stage cases, edit the Zod schema and run:",
    "//   pnpm surface:codegen --only=generate-activity-attributes",
    "",
    "import ActivityKit",
    "",
    "struct MobileSurfacesActivityAttributes: ActivityAttributes, Sendable {",
    "  public struct ContentState: Codable, Hashable, Sendable {",
    contentFields,
    "  }",
    "",
    "  enum Stage: String, Codable, Hashable, Sendable {",
    stageCases,
    "  }",
    "",
    "  var surfaceId: String",
    "  var modeLabel: String",
    "}",
    "",
  ].join("\n");
}

function renderContentStateFields() {
  const fields = Object.entries(liveSurfaceActivityContentState.shape).map(
    ([name, schema]) => ({ name, swiftType: swiftTypeFor(name, schema) }),
  );
  if (fields.length === 0) {
    throw new Error(
      "liveSurfaceActivityContentState has no fields. The codegen refuses to emit an empty ContentState because ActivityKit Codable synthesis would have nothing to decode.",
    );
  }
  return fields.map((f) => `    var ${f.name}: ${f.swiftType}`).join("\n");
}

function renderStageCases() {
  const options = liveSurfaceStage.options;
  if (!Array.isArray(options) || options.length === 0) {
    throw new Error(
      "liveSurfaceStage has no enum options. Cannot emit a Stage enum with zero cases.",
    );
  }
  return options.map((opt) => `    case ${opt}`).join("\n");
}

// Map a Zod field schema to the Swift type used in ContentState. We
// special-case `stage` to the nominal `Stage` enum (not a plain String),
// which matches the existing hand-maintained file and what the
// check-activity-attributes.mjs MS003 check expects.
function swiftTypeFor(fieldName, schema) {
  if (fieldName === "stage") {
    if (!isStageEnum(schema)) {
      throw new Error(
        `Field "stage" no longer matches liveSurfaceStage. The codegen special-cases this field to the nominal Stage enum; widening it to a different shape is a contract change that needs an explicit decision.`,
      );
    }
    return "Stage";
  }
  const { expected, reason } = resolveExpectedSwiftType(schema);
  if (expected === null) {
    throw new Error(
      `Codegen does not know how to map field "${fieldName}" to a Swift type: ${reason}. Teach scripts/lib/swift-content-state.mjs#resolveExpectedSwiftType.`,
    );
  }
  return expected;
}

function isStageEnum(schema) {
  const def = schema?._zod?.def;
  if (!def || def.type !== "enum") return false;
  const want = new Set(liveSurfaceStage.options);
  const got = new Set(def.entries ? Object.values(def.entries) : []);
  if (want.size !== got.size) return false;
  for (const v of want) if (!got.has(v)) return false;
  return true;
}
