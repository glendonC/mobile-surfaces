#!/usr/bin/env node
// Generates packages/surface-contracts/src/traps-data.ts from data/traps.json.
// The catalog is the source of truth; this generator gives the contracts
// package a typed runtime export so the harness, future MCP tooling, and
// any consumer can look up trap details by id or by error class name without
// re-reading data/traps.json themselves.
//
// `--check` mode fails CI on drift; wired into surface:check.
import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { resolve } from "node:path";
import { parseArgs } from "node:util";
import { trapCatalog } from "../packages/surface-contracts/src/traps.ts";
import { buildReport, emitDiagnosticReport } from "./lib/diagnostics.mjs";

const { values } = parseArgs({
  options: {
    check: { type: "boolean", default: false },
    json: { type: "boolean", default: false },
  },
});

const TOOL = "generate-traps-data";
const TRAPS_PATH = resolve("data/traps.json");
const OUT_PATH = resolve("packages/surface-contracts/src/traps-data.ts");

const raw = readFileSync(TRAPS_PATH, "utf8");
const parsed = JSON.parse(raw);
const result = trapCatalog.safeParse(parsed);
if (!result.success) {
  emitDiagnosticReport(
    buildReport(TOOL, [
      {
        id: "validate-traps",
        status: "fail",
        summary:
          "data/traps.json failed Zod validation; run pnpm traps:check for details.",
      },
    ]),
    { json: values.json },
  );
}

const out = `import type { TrapEntry } from "./traps.ts";

// Generated from data/traps.json by scripts/generate-traps-data.mjs. The
// catalog is the source of truth; do not edit this file by hand. Run
// pnpm surface:check (which calls --check) to verify drift; run
// node --experimental-strip-types scripts/generate-traps-data.mjs to
// regenerate after editing data/traps.json.

export const traps: readonly TrapEntry[] = ${JSON.stringify(result.data.entries, null, 2)} as const satisfies readonly TrapEntry[];

const trapsById: Record<string, TrapEntry> = Object.fromEntries(
  traps.map((entry) => [entry.id, entry]),
);

const trapsByErrorClass: Record<string, TrapEntry> = (() => {
  const out: Record<string, TrapEntry> = {};
  for (const entry of traps) {
    if (!entry.errorClasses) continue;
    for (const className of entry.errorClasses) {
      // Uniqueness is enforced by the catalog Zod schema, so the last-write
      // here would only happen if the catalog passed validation; this
      // re-check is redundant but cheap.
      out[className] = entry;
    }
  }
  return out;
})();

/**
 * Look up a trap by its catalog id (e.g. "MS018"). Returns undefined for
 * unknown ids.
 */
export function findTrap(id: string): TrapEntry | undefined {
  return trapsById[id];
}

/**
 * Look up a trap by the name of an error class that surfaces it (e.g.
 * "TopicDisallowedError"). Returns undefined for classes with no catalog
 * binding (most SDK self-correctness errors).
 */
export function findTrapByErrorClass(className: string): TrapEntry | undefined {
  return trapsByErrorClass[className];
}
`;

if (values.check) {
  const current = existsSync(OUT_PATH) ? readFileSync(OUT_PATH, "utf8") : "";
  const inSync = current === out;
  emitDiagnosticReport(
    buildReport(TOOL, [
      {
        id: "traps-data-sync",
        status: inSync ? "ok" : "fail",
        summary: inSync
          ? "Traps data export is in sync with data/traps.json."
          : "packages/surface-contracts/src/traps-data.ts is out of sync with data/traps.json.",
        ...(inSync
          ? {}
          : {
              detail: {
                message:
                  "Run: node --experimental-strip-types scripts/generate-traps-data.mjs",
              },
            }),
      },
    ]),
    { json: values.json },
  );
} else {
  writeFileSync(OUT_PATH, out);
  if (values.json) {
    emitDiagnosticReport(
      buildReport(TOOL, [
        {
          id: "traps-data-write",
          status: "ok",
          summary: "Wrote packages/surface-contracts/src/traps-data.ts.",
        },
      ]),
      { json: true },
    );
  } else {
    console.log("Wrote packages/surface-contracts/src/traps-data.ts.");
  }
}
