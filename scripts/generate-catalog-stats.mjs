#!/usr/bin/env node
// Codegen for the catalog headline counts. Source of truth: data/traps.json
// (rule severity / detection / deprecated) joined with scripts/lib/
// check-registry.mjs (which rules run in surface:check).
//
// Writes two kinds of artifact:
//
// 1. data/catalog-stats.json
//    The canonical breakdown - total, live, deprecated, the severity and
//    detection splits, and prGated. Committed so the numbers are inspectable
//    in one place and a future consumer (the mobile-surfaces CLI, MCP
//    tooling) can read structured stats instead of scraping prose.
//
// 2. The <!-- catalog-stats:KEY --> marker blocks in README.md and
//    apps/site/src/content/docs/vs-expo-live-activity.md. The generator
//    rewrites the text between each open/close pair so a rule added,
//    retired, or reclassified cannot leave a stale number in published
//    prose. Prose outside the markers is hand-authored and untouched.
//
// Pass --check to compare on-disk against the regenerated output and exit
// non-zero on drift (mirrors scripts/build-schema.mjs --check). Wired into
// scripts/lib/check-registry.mjs at trap MS044 so a hand edit to a generated
// number fails the stage-6 drift gate.
import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { resolve } from "node:path";
import { parseArgs } from "node:util";
import { trapCatalog } from "../packages/surface-contracts/src/traps.ts";
import { checkRegistry } from "./lib/check-registry.mjs";
import {
  computeCatalogStats,
  markerValues,
  applyCatalogMarkers,
} from "./lib/catalog-stats.mjs";
import { buildReport, emitDiagnosticReport } from "./lib/diagnostics.mjs";

const { values } = parseArgs({
  options: {
    check: { type: "boolean", default: false },
    json: { type: "boolean", default: false },
  },
});

const TOOL = "generate-catalog-stats";

const CANONICAL_HINT =
  "Numbers are generated from data/traps.json. Run pnpm surface:codegen " +
  "(or node --experimental-strip-types scripts/generate-catalog-stats.mjs) " +
  "to regenerate. Never hand-edit a number inside a catalog-stats: marker.";

// Validate the catalog before counting, so a malformed traps.json fails here
// with a clear message instead of producing wrong numbers silently.
const catalogPath = resolve("data/traps.json");
const parsed = JSON.parse(readFileSync(catalogPath, "utf8"));
const result = trapCatalog.safeParse(parsed);
if (!result.success) {
  console.error(
    "data/traps.json failed validation. Run validate-trap-catalog first.",
  );
  for (const issue of result.error.issues) {
    const path = issue.path.length ? issue.path.join(".") : "(root)";
    console.error(`  ${path}: ${issue.message}`);
  }
  process.exit(1);
}

const stats = computeCatalogStats(result.data, checkRegistry);
const markers = markerValues(stats);

const STATS_OUT = resolve("data/catalog-stats.json");
const statsJson =
  JSON.stringify(
    {
      $generated:
        "GENERATED by scripts/generate-catalog-stats.mjs from data/traps.json + scripts/lib/check-registry.mjs. Do not edit; run pnpm surface:codegen.",
      ...stats,
    },
    null,
    2,
  ) + "\n";

const DOC_TARGETS = [
  {
    id: "readme-markers",
    label: "README.md",
    path: resolve("README.md"),
    keys: ["userFacing", "maintenance", "prGated"],
  },
  {
    id: "vs-expo-markers",
    label: "vs-expo-live-activity.md",
    path: resolve("apps/site/src/content/docs/vs-expo-live-activity.md"),
    keys: ["userFacing", "maintenance", "prGated", "runtime"],
  },
];

// Compute each doc's rewritten form once. A missing marker is recorded as an
// error string rather than thrown so --check can report every drift in one
// pass instead of aborting on the first.
const docResults = DOC_TARGETS.map((target) => {
  const current = existsSync(target.path)
    ? readFileSync(target.path, "utf8")
    : null;
  if (current === null) {
    return { ...target, error: `${target.label} does not exist.` };
  }
  try {
    const next = applyCatalogMarkers(
      current,
      target.keys,
      markers,
      target.label,
    );
    return { ...target, current, next };
  } catch (err) {
    return { ...target, current, error: String(err?.message ?? err) };
  }
});

if (values.check) {
  const checks = [];

  const currentJson = existsSync(STATS_OUT)
    ? readFileSync(STATS_OUT, "utf8")
    : null;
  const jsonDrift = currentJson !== statsJson;
  checks.push({
    id: "catalog-stats-json",
    status: jsonDrift ? "fail" : "ok",
    summary: jsonDrift
      ? "data/catalog-stats.json drifted from the trap catalog."
      : "data/catalog-stats.json is in sync with the trap catalog.",
    trapId: "MS044",
    ...(jsonDrift
      ? {
          detail: {
            message: CANONICAL_HINT,
            issues: [
              { path: STATS_OUT, message: "catalog-stats.json out of date" },
            ],
          },
        }
      : {}),
  });

  for (const doc of docResults) {
    if (doc.error) {
      checks.push({
        id: doc.id,
        status: "fail",
        summary: `${doc.label}: ${doc.error}`,
        trapId: "MS044",
        detail: {
          message: CANONICAL_HINT,
          issues: [{ path: doc.path, message: doc.error }],
        },
      });
      continue;
    }
    const drift = doc.current !== doc.next;
    checks.push({
      id: doc.id,
      status: drift ? "fail" : "ok",
      summary: drift
        ? `${doc.label} catalog-stats markers drifted from the trap catalog.`
        : `${doc.label} catalog-stats markers are in sync with the trap catalog.`,
      trapId: "MS044",
      ...(drift
        ? {
            detail: {
              message: CANONICAL_HINT,
              issues: [
                { path: doc.path, message: "marker numbers out of date" },
              ],
            },
          }
        : {}),
    });
  }

  emitDiagnosticReport(buildReport(TOOL, checks), { json: values.json });
} else {
  // Write mode. A missing marker is fatal: regenerating around an absent
  // marker would silently leave a stale number behind.
  for (const doc of docResults) {
    if (doc.error) {
      console.error(`${doc.label}: ${doc.error}`);
      process.exit(1);
    }
  }
  writeFileSync(STATS_OUT, statsJson);
  const written = [STATS_OUT];
  for (const doc of docResults) {
    if (doc.current !== doc.next) {
      writeFileSync(doc.path, doc.next);
      written.push(doc.path);
    }
  }
  if (values.json) {
    emitDiagnosticReport(
      buildReport(TOOL, [
        {
          id: "catalog-stats-write",
          status: "ok",
          summary: `Wrote ${written.length} catalog-stats output(s).`,
        },
      ]),
      { json: true },
    );
  } else {
    for (const p of written) {
      console.log(`Wrote ${p.replace(`${process.cwd()}/`, "")}.`);
    }
  }
}
